import FizzBuzz

assert(FizzBuzz.solution(1) == "1")
assert(FizzBuzz.solution(2) == "1 2")
assert(FizzBuzz.solution(3) == "1 2 Fizz")
assert(FizzBuzz.solution(10) == "1 2 Fizz 4 Buzz Fizz 7 8 Fizz Buzz")
assert(FizzBuzz.solution(15) == "1 2 Fizz 4 Buzz Fizz 7 8 Fizz Buzz 11 Fizz 13 14 FizzBuzz")
assert(FizzBuzz.solution(18) == "1 2 Fizz 4 Buzz Fizz 7 8 Fizz Buzz 11 Fizz 13 14 FizzBuzz 16 17 Fizz")